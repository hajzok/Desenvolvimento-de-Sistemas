    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        
        int response = JOptionPane.showConfirmDialog(null, "Deseja gravar os dados ?", "ConfirmaÃ§Ã£o", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (response == JOptionPane.YES_OPTION){
            
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            String linha[] = new String [4];
            
            linha[0] = edtCodigo.getText();
            linha[1] = edtNome.getText();
            linha[2] = edtTelefone.getText();
            linha[3] = edtEmail.getText();
            
            modelo.addRow(linha);
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
        int response = JOptionPane.showConfirmDialog(null, "Deseja remover o registro ?", "ConfirmaÃ§Ã£o", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (response == JOptionPane.YES_OPTION){
            
          DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();
          
          if(jTable1.getSelectedRow()>=0){
              modelo.removeRow(jTable1.getSelectedRow());
              jTable1.setModel(modelo);
          }
          else{
              JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
          }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

