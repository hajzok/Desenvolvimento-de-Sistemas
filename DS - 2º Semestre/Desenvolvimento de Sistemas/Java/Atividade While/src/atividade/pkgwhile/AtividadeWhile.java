package atividade.pkgwhile;
public class AtividadeWhile {
public static void main(String[] args) {
        int num, num2 = 2, res;
        num = 1;
        
        for (num=1; num<=10; num-- ){
            
            res=(num * num2);
            
            System.out.printf("%dx%d=%d\n", num2, num, res);
        }
            
          
        
        
        
    }
    
}
